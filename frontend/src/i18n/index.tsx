type Params = Record<string, string | number>;
type I18nValue = { locale: Locale; setLocale: (locale: Locale) => void; t: (key: TranslationKey, params?: Params) => string };

const normalizeLocale = (value?: string | null): Locale | null => {
  if (!value) return null;
  return value.toLowerCase().startsWith('zh') ? 'zh-CN' : value.toLowerCase().startsWith('en') ? 'en-US' : null;
};

export const localeStorageKey = 'palpanel.locale';

export const detectLocale = (): Locale => {
  if (typeof window === 'undefined') return 'zh-CN';
  const saved = normalizeLocale(window.localStorage.getItem(localeStorageKey));
  if (saved) return saved;
  for (const language of window.navigator.languages || [window.navigator.language]) {
    const locale = normalizeLocale(language);
    if (locale) return locale;
  }
  return 'en-US';
};

const translate = (locale: Locale, key: TranslationKey, params?: Params) => {
  const dictionary = locale === 'en-US' ? enUS : zhCN;
  return (dictionary[key] || zhCN[key]).replace(/\{(\w+)\}/g, (_, name: string) => String(params?.[name] ?? `{${name}}`));
};

const I18nContext = createContext<I18nValue>({ locale: 'zh-CN', setLocale: () => undefined, t: (key, params) => translate('zh-CN', key, params) });

export const I18nProvider: React.FC<React.PropsWithChildren> = ({ children }) => {
  const [locale, setLocale] = useState<Locale>(detectLocale);
  useEffect(() => {
    document.documentElement.lang = locale;
    window.localStorage.setItem(localeStorageKey, locale);
  }, [locale]);
  const value = useMemo<I18nValue>(() => ({ locale, setLocale, t: (key, params) => translate(locale, key, params) }), [locale]);
  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
};

export const useI18n = () => useContext(I18nContext);
